import pandas as pd
import pytest

from src.data.indicators import IndicatorEngine, IndicatorResult


# ---------------------------------------------------------------------------
# Synthetic data factory
# ---------------------------------------------------------------------------


def make_ohlcv_df(n: int = 200, trend: str = "flat") -> pd.DataFrame:
    """
    Generate a synthetic OHLCV DataFrame with *n* rows.

    Parameters
    ----------
    n     : Number of candles to produce.
    trend : "up"   — prices rise monotonically with small noise
            "down" — prices fall monotonically with small noise
            "flat" — prices oscillate around a fixed level
    """
    rng = np.random.default_rng(seed=42)

    base = 500.0

    if trend == "up":
        # Steady uptrend: each close is ~0.3% higher than the previous.
        drift = np.linspace(0, n * 0.3, n)
        noise = rng.normal(0, 0.1, n)
        closes = base + drift + noise

    elif trend == "down":
        # Steady downtrend: each close is ~0.3% lower than the previous.
        drift = np.linspace(0, -(n * 0.3), n)
        noise = rng.normal(0, 0.1, n)
        closes = base + drift + noise

    else:  # "flat"
        # Oscillates around the base with moderate noise.
        noise = rng.normal(0, 2.0, n)
        closes = base + noise

    # Ensure prices are always positive.
    closes = np.clip(closes, 1.0, None)

    # Build OHLCV from closes; keep spreads realistic.
    spread = rng.uniform(0.2, 1.0, n)
    highs = closes + spread
    lows = np.clip(closes - spread, 0.1, None)
    opens = closes + rng.uniform(-0.5, 0.5, n)
    opens = np.clip(opens, lows, highs)
    volumes = rng.integers(100_000, 1_000_000, n).astype(float)

    timestamps = pd.date_range(
        start="2024-01-01 09:15:00",
        periods=n,
        freq="5min",
        tz="Asia/Kolkata",
    )

    return pd.DataFrame(
        {
            "timestamp": timestamps,
            "open": opens,
            "high": highs,
            "low": lows,
            "close": closes,
            "volume": volumes,
        }
    )


