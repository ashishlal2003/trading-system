class TestIndicatorEngine:
    """Test suite for IndicatorEngine.compute()."""

    # ------------------------------------------------------------------
    # Fixture
    # ------------------------------------------------------------------

    @pytest.fixture
    def engine(self) -> IndicatorEngine:
        return IndicatorEngine()

    # ------------------------------------------------------------------
    # Guard: short DataFrames
    # ------------------------------------------------------------------

    def test_compute_returns_none_on_short_df(self, engine: IndicatorEngine) -> None:
        """DataFrames with fewer than 30 rows must return None, not raise."""
        for row_count in [0, 1, 10, 29]:
            df = make_ohlcv_df(n=row_count) if row_count > 0 else pd.DataFrame(
                columns=["timestamp", "open", "high", "low", "close", "volume"]
            )
            result = engine.compute(df, symbol="TEST")
            assert result is None, (
                f"Expected None for {row_count} rows, got {result!r}"
            )

    def test_compute_returns_none_on_none_df(self, engine: IndicatorEngine) -> None:
        """Passing None as df must return None safely."""
        result = engine.compute(None, symbol="TEST")  # type: ignore[arg-type]
        assert result is None

    # ------------------------------------------------------------------
    # Happy path: 200-row realistic DataFrame
    # ------------------------------------------------------------------

    def test_compute_returns_indicator_result(self, engine: IndicatorEngine) -> None:
        """200 rows of realistic OHLCV data must return a valid IndicatorResult."""
        df = make_ohlcv_df(n=200, trend="flat")
        result = engine.compute(df, symbol="RELIANCE")

        assert result is not None
        assert isinstance(result, IndicatorResult)
        assert result.symbol == "RELIANCE"
        assert isinstance(result.timestamp, (pd.Timestamp, type(None)))

    # ------------------------------------------------------------------
    # RSI range
    # ------------------------------------------------------------------

    def test_rsi_in_valid_range(self, engine: IndicatorEngine) -> None:
        """
        RSI(14) must be in [0, 100] for any trend that contains both up and down candles.

        A pure monotonic uptrend can produce RSI=NaN (no down-candles → avg_loss=0 →
        division by zero in the RS formula). We test with "flat" (always mixed) and
        "down" (losses dominate → RSI approaches 0 but stays numeric). We separately
        verify the "up" case via a mixed-noise dataset where NaN is not expected.
        """
        import math

        for trend in ("down", "flat"):
            df = make_ohlcv_df(n=200, trend=trend)
            result = engine.compute(df, symbol=f"SYM_{trend.upper()}")
            assert result is not None
            assert not math.isnan(result.rsi_14), (
                f"RSI is NaN for trend={trend!r}"
            )
            assert 0.0 <= result.rsi_14 <= 100.0, (
                f"RSI out of range for trend={trend!r}: {result.rsi_14}"
            )

        # For a mixed-noise uptrend (noise large enough to produce down-candles),
        # RSI should also be finite and in range.
        rng = np.random.default_rng(seed=99)
        n = 200
        closes = 500.0 + np.linspace(0, 30, n) + rng.normal(0, 2.0, n)
        spread = rng.uniform(0.2, 1.0, n)
        highs = closes + spread
        lows = np.clip(closes - spread, 0.1, None)
        opens = np.clip(closes + rng.uniform(-0.5, 0.5, n), lows, highs)
        volumes = rng.integers(100_000, 1_000_000, n).astype(float)
        timestamps = pd.date_range("2024-01-01 09:15", periods=n, freq="5min", tz="Asia/Kolkata")
        mixed_df = pd.DataFrame({
            "timestamp": timestamps, "open": opens, "high": highs,
            "low": lows, "close": closes, "volume": volumes,
        })
        result = engine.compute(mixed_df, symbol="SYM_MIXED_UP")
        assert result is not None
        assert not math.isnan(result.rsi_14), "RSI is NaN for mixed uptrend"
        assert 0.0 <= result.rsi_14 <= 100.0, f"RSI out of range: {result.rsi_14}"

    # ------------------------------------------------------------------
    # MACD histogram identity
    # ------------------------------------------------------------------

    def test_macd_histogram_equals_line_minus_signal(
        self, engine: IndicatorEngine
    ) -> None:
        """macd_hist must equal macd_line − macd_signal within float precision."""
        df = make_ohlcv_df(n=200, trend="flat")
        result = engine.compute(df, symbol="TEST")
        assert result is not None

        expected_hist = result.macd_line - result.macd_signal
        assert math.isclose(result.macd_hist, expected_hist, rel_tol=1e-9, abs_tol=1e-12), (
            f"MACD histogram identity violated: "
            f"hist={result.macd_hist}, line-signal={expected_hist}"
        )

    # ------------------------------------------------------------------
    # EMA ordering — uptrend
    # ------------------------------------------------------------------

    def test_ema_ordering_uptrend(self, engine: IndicatorEngine) -> None:
        """
        When prices rise steadily ema_9 > ema_21 > ema_50 and
        the trend string must be 'UPTREND'.
        """
        df = make_ohlcv_df(n=200, trend="up")
        result = engine.compute(df, symbol="UPTREND_SYM")
        assert result is not None

        assert result.ema_9 > result.ema_21, (
            f"Expected ema_9 > ema_21 in uptrend: {result.ema_9} <= {result.ema_21}"
        )
        assert result.ema_21 > result.ema_50, (
            f"Expected ema_21 > ema_50 in uptrend: {result.ema_21} <= {result.ema_50}"
        )
        assert result.trend == "UPTREND", (
            f"Expected trend='UPTREND', got '{result.trend}'"
        )

    # ------------------------------------------------------------------
