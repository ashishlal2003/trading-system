    # ------------------------------------------------------------------
    # to_dict serialisability
    # ------------------------------------------------------------------

    def test_to_dict_returns_serialisable_dict(self, engine: IndicatorEngine) -> None:
        """to_dict() must return a plain dict with all expected keys."""
        df = make_ohlcv_df(n=200)
        result = engine.compute(df, symbol="DICT_TEST")
        assert result is not None

        d = result.to_dict()
        assert isinstance(d, dict)

        expected_keys = {
            "symbol", "timestamp", "close", "rsi_14",
            "macd_line", "macd_signal", "macd_hist",
            "ema_9", "ema_21", "ema_50", "ema_200",
            "bb_upper", "bb_mid", "bb_lower", "bb_pct_b",
            "atr_14", "volume_sma_20", "relative_volume",
            "vwap", "trend", "price_vs_vwap",
        }
        assert expected_keys.issubset(d.keys()), (
            f"Missing keys in to_dict() output: {expected_keys - d.keys()}"
        )
        # Timestamp should be converted to a string
        assert isinstance(d["timestamp"], str), (
            f"Expected timestamp as str, got {type(d['timestamp'])}"
        )

